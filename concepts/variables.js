//Declaración de variables con let y const
//var: en desuso

//variables let: cambiarán de valor
let age; //declaración
console.log(age);
age = 23; //inicialización
console.log(age);

let name = "Brian"; //declaración e inicialización
console.log(name);
name = "Tavo";
console.log(name);

//constantes const: no cambiarán de valor
const PI = 3.1416;
//PI = 3.15;
console.log(PI);